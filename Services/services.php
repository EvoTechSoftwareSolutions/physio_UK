                    <br>
                    Injuries can vary in severity and musculoskeletal pain. It can be anything from a minor twinge to debilitating agony. In order to ascertain the extent of your injuries you will need a musculoskeletal assessment of your pain and/or injury. Then your physio will be able to use a number of skills that can include manual therapy, acupuncture, massage and design a rehabilitation programme to aid your recovery.
                    <br>
                    The aim of sport physiotherapy is to identify the sport injury and work with you to restore your body to full health and back to sport. To do this, the sport physiotherapist will devise a recovery plan that includes numerous musculoskeletal physical therapy tools, as above including sport injury massage, rehabilitation exercise, dietary plans and more. The treatment will be more sports specific.
